#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*a;

	a = (unsigned char *)s;
	while ((n-- > 0))
	{
		if ((unsigned char)c == *a++)
			return (a - 1);
	}
	return (0);
}
